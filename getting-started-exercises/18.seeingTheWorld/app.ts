
let places: string[] = ["switzerland","Peru","Usa","Canada","Malaysia"]
console.log(places)
//alphabetical order
console.log([...places].sort())
//original order
console.log(places)
//reverse order
console.log([...places].sort().reverse())
//original order
console.log(places)
//reverse the original array
places.reverse()
console.log(places)
//reverse again
places.reverse();
console.log(places)
//sort alphabetic
places.sort()
console.log(places)
//reverse alphabetic
places.sort().reverse()
console.log(places)